            <div class="post-content-body">
              <p>The situation we face now, with the new coronavirus, can limit many scientific activities. 
              Since most of us are advised to stay home, most field studies are suspended. Yet, there is still a 
              lot to research in such challenging times, including in scientific fields beyond epidemiology and 
              public health, which, we hope will advance significantly in the coming months.</p>

            <p>As governments and authorities urge most of us to self-isolate and to remain in quarantine, cities
             are becoming quitter. Cities are becoming less polluted by traffic and industrial activities 
             (as shown by NASA satellite images).</p>

            <p>In addition to reduction of air pollution and quieter cities, people are spending more time at home,
             and in the few spaces in their houses that link them to the external world – such as windows 
             and porches. In this new scenario, we make two potential predictions, that are now mutually 
             exclusive: biodiversity might reclaim parts of the cities in the absence of dense human occupation, 
             and more people are likely to notice the biodiversity that share urban spaces with us. The reduction 
             in anthropogenic noise can also lead to an increased ability to notice the biophonies (the natural noises) 
             in our cities.</p>

            <p>With that in mind, Fonotropica (UFBA's digital collection of nature sounds) is proposing a 
            citizen science initiative to try to answer the following question: “Will we better listen to the 
            biodiversity of our windows in these days of seclusion?”</p>

            <p>Our team of researchers is interested in knowing what the acoustic landscape will look like during 
            and after the period of population isolation. In other words, are we likely to experience an 
            increase in the acoustic complexity of the urban landscape as a side effect of quarantine? 
            Could this moment in history serve as an opportunity to (re)connect with nature through the 
            sounds of nature coming through our windows? </p>

            <p>All you need to help us, and to become more tuned in to the biodiversity 
            near your home is (1) a window and (2) a cell phone! Pretty easy, right? If you 
            want to participate, please access the instructions below to learn how you can register 
            your window in our database and send us your recordings!</p>

            <h5 class="mb-4">INSTRUCTIONS TO PARTICIPATE IN THE PROJECT:</h5>

            <p>Each participant should choose one window / point in their garden where the sounds will be recorded from.
             Then, participants need to fill up the form following this spreadsheet:  <a class="btn-link" href="https://forms.gle/dSF8UtpsxC5gN6228">https://forms.gle/dSF8UtpsxC5gN6228</a></a></p>

            <p>
            After registration, participants will receive a registration number which will become their ‘window number’.
             All recordings (audio files) should be sent in the email:  <a href="mailto:fonotropica@gmail.com?subject=Questions" title="">fonotropica@gmail.com</a>
            </p>

            <p>Piece of cake, right?</p>

            <p>If you are interested, please read further to understand how to set up your 
            phone to increase audio quality and how to ensure that your files match our database requirements! </p>

            <h5 class="mb-4">How should I record my audios?</h5>

            <p>Recordings can be made using a cell phone and should last for five minutes uninterrupted! 
            We recommend using the Rode “Reporter” app for Iphone and the RecForge II for Android (both available for free). 
            These apps enable recordings with the extension "wav", which is the required format for our database! 
            Please adjust the format in settings to make sure you are recording in wav, not in mp3.</p>

            <p>
            Now tips to improve the quality of your recordings: In general, you will be making landscape recordings, 
            meaning you will capture a range of sounds from the environment. 
            To do so, avoid being too close to continuous noise, such as a fan, the TV, dogs barking, etc. 
            If someone (including your dog) makes loud noises during your recordings, 
            please start over so we can capture more of wildlife! You can also play with the settings to adjust 
            what works better in your scenario. Avoid having people talking in the back, or the TV on. 
            It is also important that you don’t move while recording, and that the phone is facing out, 
            to capture the noise coming from the outside world. It is only five minutes! You got this! : )
            </p>

            <h5 class="mb-4">What time of the day should I be recording?</h5>

            <p>We have divided the day into 6 periods of four hours each (00h - 3h59min, 4h to 7h59min, 8h to 11h59min; 
            12h to 15h59min; 16h to 19h59min; 20h to 23h59min). You can record any time of the day, 
            or even many times of the day, as long as the recordings are in different periods. Also, 
            you should not record multiple times in the same period, during the same day. It means that you can 
            send up to 6 recordings per day, as long as each recording was made in a different period. 
            Or that you can select a period and do your recordings every day more of less in the same time. 
            </p>

            <h5 class="mb-4">What should I do / where should I send my recordings again?</h5>

